package com.jsp.dto;

public class User {
	private int id;
	private String name;
	private int age;
	private long phN;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public long getPhN() {
		return phN;
	}
	public void setPhN(long phN) {
		this.phN = phN;
	}
	
}
